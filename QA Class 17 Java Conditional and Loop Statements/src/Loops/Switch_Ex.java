package Loops;

public class Switch_Ex {

	public static void main(String[] args) {
		
		char grade='Z';
		
		switch(grade) 
		{
		
		case 'A':
			System.out.println("FIRST GRADE");
			break;
		case 'B':
			System.out.println("SECOND GRADE");
			break;
		case 'C':
			System.out.println("THIRD GRADE");
			break;
		default:
			System.out.println("INVALID GRADE");
		break;
		
		}
		

	}

}
