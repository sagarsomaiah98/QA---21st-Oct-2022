package Static_KeyWords;

public class Cust {
	
	public void cust_info() {
		
		System.out.println("calling cust_info");
	}
	
	public void cust_salary() {
		
		System.out.println("calling cust_Salary");
	}

	public static void main(String[] args) {
		
		Cust c = new Cust();
		c.cust_info();
		c.cust_salary();
	}

}
